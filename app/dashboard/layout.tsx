'use client';

import { useEffect, useState, useCallback } from 'react';
import { useRouter, usePathname } from 'next/navigation';
import Link from 'next/link';
import Image from 'next/image';
import { useAuthStore } from '@/lib/auth-store';
import { cn } from '@/lib/utils';
import { Button } from '@/components/ui/button';
import { Avatar, AvatarFallback, AvatarImage } from '@/components/ui/avatar';
import { GlobalSearch } from '@/components/global-search';
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuLabel,
  DropdownMenuSeparator,
  DropdownMenuTrigger,
} from '@/components/ui/dropdown-menu';
import {
  FolderKanban,
  CheckSquare,
  MessageSquare,
  FileText,
  BarChart3,
  LogOut,
  Menu,
  X,
  User,
  ChevronLeft,
  Plus,
  Home,
  Search,
} from 'lucide-react';
import { TimerIndicator } from '@/components/timer-indicator';
import { NotificationsDropdown } from '@/components/notifications-dropdown';
import { ChatNotificationProvider } from '@/components/chat/chat-notification-provider';
import { AIChatBot } from '@/components/ai';
import { TeamChatSidebar } from '@/components/team-chat-sidebar';
import { ThemeToggle } from '@/components/theme-toggle';

const navigation = [
  { name: 'Dashboard', href: '/dashboard', icon: Home },
  { name: 'Projects', href: '/dashboard/projects', icon: FolderKanban, permissions: ['projects.view'] },
  { name: 'Tasks', href: '/dashboard/tasks', icon: CheckSquare, permissions: ['tasks.view'] },
  { name: 'Message', href: '/dashboard/chat', icon: MessageSquare },
  { name: 'Files', href: '/dashboard/files', icon: FileText, permissions: ['files.view'] },
  { name: 'Reports', href: '/dashboard/reports', icon: BarChart3, permissions: ['reports.view'] },
];

// Map routes to page titles
const pageTitles: Record<string, string> = {
  '/dashboard': 'Dashboard',
  '/dashboard/projects': 'Projects',
  '/dashboard/tasks': 'Tasks',
  '/dashboard/team': 'Team',
  '/dashboard/chat': 'Message',
  '/dashboard/files': 'Files',
  '/dashboard/reports': 'Reports',
  '/dashboard/activity': 'Activity',
};

export default function DashboardLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const router = useRouter();
  const pathname = usePathname();
  const { user, isAuthenticated, isLoading, logout, hasAnyPermission, fetchPermissions, permissions } = useAuthStore();
  const [sidebarOpen, setSidebarOpen] = useState(false);
  const [mobileSearchOpen, setMobileSearchOpen] = useState(false);
  const [sidebarCollapsed, setSidebarCollapsed] = useState(false);
  const [teamSidebarExpanded, setTeamSidebarExpanded] = useState(true);

  // Persist team sidebar state in localStorage
  useEffect(() => {
    const saved = localStorage.getItem('teamSidebarExpanded');
    if (saved !== null) setTeamSidebarExpanded(saved === 'true');
  }, []);

  const toggleTeamSidebar = useCallback(() => {
    setTeamSidebarExpanded((prev) => {
      const next = !prev;
      localStorage.setItem('teamSidebarExpanded', String(next));
      return next;
    });
  }, []);

  // Get current page title
  const getCurrentPageTitle = () => {
    for (const [path, title] of Object.entries(pageTitles)) {
      if (pathname === path || (path !== '/dashboard' && pathname.startsWith(path))) {
        return title;
      }
    }
    return 'Dashboard';
  };

  // Check if a navigation item is active
  const isActiveLink = (href: string) => {
    if (href === '/dashboard') {
      return pathname === '/dashboard';
    }
    return pathname.startsWith(href);
  };

  // Fetch permissions on mount if authenticated
  useEffect(() => {
    if (isAuthenticated && permissions.length === 0) {
      fetchPermissions();
    }
  }, [isAuthenticated, permissions.length, fetchPermissions]);

  // Filter navigation based on permissions
  const filteredNavigation = navigation.filter((item) => {
    if (!('permissions' in item) || !item.permissions?.length) return true;
    if (user?.role === 'ADMIN') return true;
    return hasAnyPermission(item.permissions);
  });

  useEffect(() => {
    if (!isLoading && !isAuthenticated) {
      router.replace('/login');
    }
  }, [isAuthenticated, isLoading, router]);

  if (isLoading) {
    return (
      <div className="flex h-screen items-center justify-center bg-[hsl(var(--layout-bg))]">
        <div className="flex flex-col items-center gap-4">
          <div className="h-8 w-8 animate-spin rounded-full border-4 border-blue-500 border-t-transparent" />
          <p className="text-[hsl(var(--text-secondary))]">Loading...</p>
        </div>
      </div>
    );
  }

  if (!isAuthenticated || !user) {
    return null;
  }

  const handleLogout = async () => {
    await logout();
    router.replace('/login');
  };

  const getInitials = (name: string) => {
    return name
      .split(' ')
      .map((n) => n[0])
      .join('')
      .toUpperCase()
      .slice(0, 2);
  };

  return (
    <ChatNotificationProvider>
      <div className="flex h-screen overflow-hidden bg-[hsl(var(--layout-bg))]">
        {/* Mobile sidebar backdrop */}
        {sidebarOpen && (
          <div
            className="fixed inset-0 z-40 bg-black/50 lg:hidden"
            onClick={() => setSidebarOpen(false)}
          />
        )}

        {/* Sidebar */}
        <aside
          className={cn(
            'fixed inset-y-0 left-0 z-50 flex flex-col bg-[hsl(var(--layout-bg))] border-r border-[hsl(var(--layout-border))] transition-all duration-300 ease-in-out lg:static',
            sidebarCollapsed ? 'w-20' : 'w-64',
            sidebarOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'
          )}
        >
          {/* Logo Section */}
          <div className={cn(
            'flex h-16 items-center justify-between px-4 border-b border-[hsl(var(--layout-border))]',
            sidebarCollapsed && 'justify-center px-2'
          )}>
            <Link href="/dashboard" className="flex items-center gap-3">
              <Image
                src="/logo.png"
                alt="Super In Tech"
                width={40}
                height={40}
                className="rounded-xl object-contain"
              />
              {!sidebarCollapsed && (
                <span className="font-semibold text-[hsl(var(--text-primary))] text-lg">Super In Tech</span>
              )}
            </Link>
            {!sidebarCollapsed && (
              <Button
                variant="ghost"
                size="icon"
                className="h-8 w-8 rounded-full bg-blue-500 hover:bg-blue-600 text-white lg:flex hidden"
                onClick={() => setSidebarCollapsed(true)}
              >
                <ChevronLeft className="h-4 w-4" />
              </Button>
            )}
            <Button
              variant="ghost"
              size="icon"
              className="lg:hidden text-[hsl(var(--text-primary))]"
              onClick={() => setSidebarOpen(false)}
            >
              <X className="h-5 w-5" />
            </Button>
          </div>

          {/* Expand button when collapsed */}
          {sidebarCollapsed && (
            <div className="flex justify-center py-2 border-b border-[hsl(var(--layout-border))]">
              <Button
                variant="ghost"
                size="icon"
                className="h-8 w-8 rounded-full bg-blue-500 hover:bg-blue-600 text-white"
                onClick={() => setSidebarCollapsed(false)}
              >
                <ChevronLeft className="h-4 w-4 rotate-180" />
              </Button>
            </div>
          )}

          {/* Navigation */}
          <nav className={cn(
            'flex-1 overflow-y-auto py-4',
            sidebarCollapsed ? 'px-2' : 'px-3'
          )}>
            <div className="space-y-1">
              {filteredNavigation.map((item) => {
                const isActive = isActiveLink(item.href);
                return (
                  <Link
                    key={item.name}
                    href={item.href}
                    className={cn(
                      'flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-all duration-200 relative',
                      sidebarCollapsed && 'justify-center px-2',
                      isActive
                        ? 'bg-blue-500/10 text-blue-400'
                        : 'text-[hsl(var(--text-secondary))] hover:bg-[hsl(var(--layout-card-hover))] hover:text-[hsl(var(--text-primary))]'
                    )}
                    onClick={() => setSidebarOpen(false)}
                    title={sidebarCollapsed ? item.name : undefined}
                  >
                    {isActive && (
                      <div className="absolute left-0 top-1/2 -translate-y-1/2 w-1 h-6 bg-blue-500 rounded-r-full" />
                    )}
                    <item.icon className="h-5 w-5 shrink-0" />
                    {!sidebarCollapsed && item.name}
                  </Link>
                );
              })}
            </div>
          </nav>

        </aside>

        {/* Main content */}
        <div className="flex flex-1 flex-col overflow-hidden">
          {/* Header */}
          <header className="flex h-16 items-center justify-between bg-[hsl(var(--layout-header))] border-b border-[hsl(var(--layout-border))] px-6">
            {/* Left - Menu button (mobile) and Page Title */}
            <div className="flex items-center gap-4">
              <Button
                variant="ghost"
                size="icon"
                className="lg:hidden text-[hsl(var(--text-primary))]"
                onClick={() => setSidebarOpen(true)}
              >
                <Menu className="h-5 w-5" />
              </Button>
              <div className="flex items-center gap-3">
                <div className="w-8 h-8 rounded-lg bg-blue-500/20 flex items-center justify-center">
                  <Home className="h-4 w-4 text-blue-400" />
                </div>
                <span className="text-[hsl(var(--text-primary))] font-semibold text-lg">{getCurrentPageTitle()}</span>
              </div>
            </div>

            {/* Center - Search */}
            <div className="hidden md:flex flex-1 max-w-md mx-8">
              <GlobalSearch />
            </div>

            {/* Right - Actions */}
            <div className="flex items-center gap-3">
              {/* Mobile Search Toggle */}
              <Button
                variant="ghost"
                size="icon"
                className="md:hidden text-[hsl(var(--text-secondary))]"
                onClick={() => setMobileSearchOpen(!mobileSearchOpen)}
              >
                <Search className="h-5 w-5" />
              </Button>

              <TimerIndicator />

              {/* Mobile Quick-Create */}
              <DropdownMenu>
                <DropdownMenuTrigger asChild>
                  <Button size="icon" className="sm:hidden bg-blue-500 hover:bg-blue-600 text-white h-9 w-9">
                    <Plus className="h-4 w-4" />
                  </Button>
                </DropdownMenuTrigger>
                <DropdownMenuContent className="w-48" align="end">
                  <DropdownMenuItem onClick={() => router.push('/dashboard/projects')}>
                    <FolderKanban className="mr-2 h-4 w-4" /> New Project
                  </DropdownMenuItem>
                  <DropdownMenuItem onClick={() => router.push('/dashboard/tasks')}>
                    <CheckSquare className="mr-2 h-4 w-4" /> New Task
                  </DropdownMenuItem>
                </DropdownMenuContent>
              </DropdownMenu>

              {/* New Project Button */}
              <Button
                variant="outline"
                className="hidden sm:flex border-emerald-500/50 text-emerald-400 hover:bg-emerald-500/10 hover:text-emerald-300 hover:border-emerald-400"
                onClick={() => router.push('/dashboard/projects')}
              >
                <Plus className="h-4 w-4 mr-2" />
                New Project
              </Button>

              {/* New Task Button */}
              <Button
                className="hidden sm:flex bg-blue-500 hover:bg-blue-600 text-white"
                onClick={() => router.push('/dashboard/tasks')}
              >
                <Plus className="h-4 w-4 mr-2" />
                New Task
              </Button>

              {/* Notifications */}
              <NotificationsDropdown />

              {/* Theme Toggle */}
              <ThemeToggle />

              {/* User Avatar */}
              <DropdownMenu>
                <DropdownMenuTrigger asChild>
                  <Button variant="ghost" className="relative h-10 w-10 rounded-full p-0">
                    <Avatar className="h-10 w-10 border-2 border-[hsl(var(--layout-border))]">
                      <AvatarImage src={user.avatar} alt={user.name} />
                      <AvatarFallback className="bg-gradient-to-br from-amber-400 to-orange-500 text-white font-medium">
                        {getInitials(user.name)}
                      </AvatarFallback>
                    </Avatar>
                  </Button>
                </DropdownMenuTrigger>
                <DropdownMenuContent className="w-56 bg-[hsl(var(--layout-card))] border-[hsl(var(--layout-border))] text-[hsl(var(--text-primary))]" align="end" forceMount>
                  <DropdownMenuLabel className="font-normal">
                    <div className="flex flex-col space-y-1">
                      <p className="text-sm font-medium leading-none text-[hsl(var(--text-primary))]">{user.name}</p>
                      <p className="text-xs leading-none text-[hsl(var(--text-secondary))]">{user.email}</p>
                      <p className="text-xs leading-none text-blue-400 font-medium mt-1">{user.role}</p>
                    </div>
                  </DropdownMenuLabel>
                  <DropdownMenuSeparator className="bg-[hsl(var(--layout-border))]" />
                  <DropdownMenuItem asChild className="text-[hsl(var(--text-secondary))] focus:bg-[hsl(var(--layout-card-hover))] focus:text-[hsl(var(--text-primary))]">
                    <Link href="/dashboard/team" className="flex items-center cursor-pointer">
                      <User className="mr-2 h-4 w-4" />
                      My Profile
                    </Link>
                  </DropdownMenuItem>
                  <DropdownMenuSeparator className="bg-[hsl(var(--layout-border))]" />
                  <DropdownMenuItem
                    onClick={handleLogout}
                    className="text-red-400 focus:bg-red-500/10 focus:text-red-400"
                  >
                    <LogOut className="mr-2 h-4 w-4" />
                    Log out
                  </DropdownMenuItem>
                </DropdownMenuContent>
              </DropdownMenu>
            </div>
          </header>

          {/* Mobile Search Bar */}
          {mobileSearchOpen && (
            <div className="md:hidden px-4 py-2 bg-[hsl(var(--layout-card))] border-b border-[hsl(var(--layout-border))]">
              <GlobalSearch />
            </div>
          )}

          {/* Page content */}
          <main className="flex-1 overflow-y-auto p-4 sm:p-6 bg-[hsl(var(--layout-bg))]">{children}</main>
        </div>

        {/* Team Chat Sidebar - always visible */}
        <TeamChatSidebar expanded={teamSidebarExpanded} onToggle={toggleTeamSidebar} />
      </div>

      {/* AI Chat Bot - Floating, positioned to avoid sidebar overlap */}
      <AIChatBot sidebarCollapsed={sidebarCollapsed} />
    </ChatNotificationProvider>
  );
}
